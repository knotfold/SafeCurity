    /**
     * A placeholder fragment containing a simple view.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @SuppressLint("Assert")
        @Override
        public Fragment getItem(int position) {
            switch(position)
            {
                case 0:
                   Tab1QuickButtons tab1 = new Tab1QuickButtons();

                    return tab1;


                case 1:
                    Tab2Agenda tab2 = new Tab2Agenda();

                    return tab2;

                case 2:
                    Tab3Config tab3 = new Tab3Config();

                    return tab3;



                default:
                    return null;

            }
        }


        @Override
        public int getCount() {
            // Show 3 total pages.
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:

                    return "tab1";
                case 1:
                    return "RESTAURANTES";
                case 2:
                    return  "CONFIGURACION";
                case 3:
                    return "idk";
            }

            return null;
        }
    }
